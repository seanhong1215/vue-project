<template>
  <div class="hello">
    <a href="#" @click.prevent="signout">登出</a>
  </div>
</template>

<script>
export default {
  name: 'HelloWorld',
  data () {
    return {
      msg: "Welcome to Your VueJs App",
    };
  },
  methods: {
    signout() {
      const api = `${process.env.APIPATH}/logout`;
      const vm = this;
      this.$http.post(api).then((res) => {
        console.log(res.data);
        if (res.data.success) {
          vm.$router.push('/login');
        }
      });
    },
  },
}
</script>

